            <h2>Tajine</h2>
            <br><br>
            <p>
                Entre os vários pratos tradicionais marroquinos que também são populares no exterior está o tajine, um cozido de carne ou frango.
            </p>

            <p>
                O tajine, feito de terracota, é composto por duas partes: uma placa inferior com bordas baixas e uma tampa cônica. A tampa é feita para reter o calor durante o cozimento lento, deixando o prato macio e suculento.
            </p>

            <p>
                Os tajines mais populares são o mqualli com frango, limão e azeitonas e o mrouzia com borrego, ameixas e amêndoas.
