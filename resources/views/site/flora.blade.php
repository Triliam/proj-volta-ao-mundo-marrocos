            <h2>Todra</h2>
            <br><br>
            <p>
                Las gargantas del Todra, ubicadas en el lado este de las montañas del Alto Atlas en Marruecos, son reconocidas mundialmente por ser uno de los cañones rocosos más espectaculares del mundo.</p>

            <p>
                El agua cristalina, fresca y limpia de las gargantas atrae a muchos turistas que buscan refrescarse y disfrutar de los bellos paisajes de la zona. Además, los acantilados de la garganta son un imán para los escaladores, especialmente para los europeos, principalmente españoles y franceses, aunque actualmente se haya convertido en una enorme escuela.</p>
            <p>
                Con solo 33 metros de ancho en algunos lugares, las gargantas cuentan con acantilados de más de 100 metros de altura a cada lado del río. Aunque el río se ha secado, todavía se puede imaginar las poderosas fuerzas de la naturaleza que tallaron esta región.
            </p>

