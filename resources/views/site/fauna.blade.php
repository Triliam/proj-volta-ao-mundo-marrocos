            <h2>Linha do tempo</h2>
            <p>
                <strong>Pré-História:</strong> Os primeiros vestígios humanos em Marrocos datam de mais de 100.000 anos atrás, incluindo
                ferramentas de pedra e arte rupestre encontradas em várias partes do país.
            </p>

            <p>
                <strong>Antiguidade:</strong> Marrocos era habitado por várias tribos berberes ao longo dos séculos. Durante a Antiguidade, a região foi colonizada por fenícios, cartagineses e romanos. Cartago estabeleceu colônias na costa, enquanto os romanos conquistaram a região e a incorporaram à Província da Mauritânia Tingitana.
            </p>

            <p>
                <strong>Período Islâmico:</strong> No século VII, o Islã foi introduzido em Marrocos pelos árabes muçulmanos. O país testemunhou a fundação de várias dinastias islâmicas, incluindo os Idríssidas e os Almorávidas, que governaram partes ou toda a região.
            </p>
            <p>
                <strong>Dinastias Berberes e Reinos Islâmicos:</strong> Marrocos era governado por várias dinastias berberes e reinos islâmicos ao longo dos séculos, incluindo os Almóadas, os Merínidas e os Saadianos. Esses períodos foram marcados por uma rica história cultural, com o florescimento da arte, arquitetura e literatura.
            </p>

            <p>
                <strong>Colonização Europeia:</strong> No final do século XIX, Marrocos tornou-se objeto de disputa entre as potências coloniais europeias. Em 1912, o país foi dividido entre França e Espanha, com a França controlando a maior parte do território.
            </p>
            <p>
                <strong>Independência:</strong> Marrocos conquistou sua independência da França e da Espanha em 1956, tornando-se um estado soberano. O sultão Mohammed V tornou-se o primeiro rei de Marrocos.
            </p>

            <p>
                <strong>Reinado de Hassan II:</strong> Hassan II sucedeu ao trono em 1961 e governou o país até sua morte em 1999. Seu reinado foi marcado por reformas políticas, sociais e econômicas, bem como por eventos como a Marcha Verde de 1975, que levou à reintegração do Saara Ocidental sob o controle marroquino.
            </p>
            <p>
                <strong>Reinado de Mohammed VI:</strong> Mohammed VI assumiu o trono em 1999 e continua a ser o rei de Marrocos até hoje. Seu reinado tem sido caracterizado por reformas políticas e sociais, bem como esforços para modernizar a economia e promover os direitos humanos.
            </p>
